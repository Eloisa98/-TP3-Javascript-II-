function soma(a, b) {
    return a + b;
}

function subtracao(a, b) {
    return a - b;
}

function multiplicacao(a, b) {
    return a * b;
}

let resultadoSoma = soma(5, 3);
let resultadoSubtracao = subtracao(5, 3);
let resultadoMultiplicacao = multiplicacao(5, 3);

alert("Resultado da soma:", resultadoSoma);
alert("Resultado da subtração:", resultadoSubtracao);
alert("Resultado da multiplicação:", resultadoMultiplicacao);
